
namespace Existence.Earth.Countries.UnitedStates
{
    public static class FederalLaws
    {
        public static class v1986
        {
            /// <summary>
            /// Non-citizen voting
            /// 1. Prohibits non-citizens from voting in federal elections (e.g., Presidential, U.S. Senate, and U.S. House elections).
            /// 2. Exception: Allows lawfully admitted non-citizens (such as legal permanent residents) to vote if a state had explicitly allowed it before 1996.
            /// 3. Penalties: Includes fines, imprisonment, and potential deportation for non-citizens who violate this law.
            /// </summary>
            public static class ImmigrationReformAndControlActOf1986IRCA { }
        }

        public static class v1996
        {
            /// <summary>
            /// Non-citizen voting
            /// 1. new penalties for non-citizens who vote in state and local elections illegally
            /// 2. aggravated felony for a non-citizen to illegally vote in any election (federal, state, or local).
            /// 3. Non-citizens who vote illegally can now face deportation and be permanently barred from U.S. citizenship.
            /// 4. permitted states to require documentary proof of citizenship when registering voters, though this has been legally contested
            /// 5. Made it a federal crime for non-citizens to claim they were U.S. citizens for any purpose, including voting.
            /// </summary>
            public static class IllegalImmigrationReformAndImmigrantResponsibilityActIIRIRA { }
        }

        public static class v1993
        {
            /// <summary>
            /// allowed for "motor voter" registration at the DMV and did not require proof of citizenship
            /// </summary>
            public static class NationalVoterRegistrationActOf1993NVRA { }
        }
    }
}
