
namespace Existence.Earth.Sports.Football.American
{
    internal class Numbers
    {
        /// <summary>
        /// requires a combination of shutout and one point safety
        /// </summary>
        public class OnePointScorigamiIsEvenMoreImpossible { }

        /// <summary>
        /// A one point safety requires somehow an offensive player takes the ball backwards into their own
        /// endzone for a safety. It's contemplated in the rules but has never happened. The way the game is
        /// actually played it seems impossible
        /// </summary>
        public class OnePointSafetyIsPracticallyImpossible { }

        /// <summary>
        /// two safeties and otherwise a shutout, occurred once
        /// 1923-11-25: Racine Legion 10 at 4 Chicago Cardinals (10-4)
        /// </summary>
        public class ScoringFourPointsHasOnlyHappendOnce { }

        /// <summary>
        /// 0-0   (10/10/1920)
        /// 7-0   (10/17/1920)
        /// 36-0  (10/01/1922)
        /// 34-27 (10/24/1948)
        /// 20-16 (11/22/1953)
        /// 28-25 (10/30/1994)
        /// This seems unlikely to ever happen again given how thinned out the scorigami table is
        /// </summary>
        public class DoubleScorigamiOnSameDayHasHappenedSixTimes { }

        /// <summary>
        /// Oakland Raiders 43 at 43 Boston Patriots (October 16, 1964)
        /// </summary>
        public class FortyThreeFortyThreeMaximumTie { }

        /// <summary>
        /// The most points ever scored in an NFL game is 73, with the widest margin of victory also 73.
        /// This was done in the 1940 NFL Championship Game (Chicago Bears over the Washington Redskins).
        /// This was done using 11 touchdowns
        ///  6 extra point kicks converted
        ///  3 extra point kicks failed
        ///  1 extra point was converted using a pass (Joe Maniaci from Solly Sherman)
        ///  1 extra point pass failed
        /// </summary>
        public class SeventyThreeMaximumScoreByOneTeam { }

        /// <summary>
        /// New York Giants 41 at 72 Washington Redskins (November 26, 1966)
        /// </summary>
        public class OneHundredThirteenMaximumScoreByBothTeamsCombined {}

        /// <summary>
        /// There are seventy three 0-0 scorigamis, the latest occurring in 1943. There is a lot of scoring in
        /// the NFL these days (and an overtime period to boot), so this seems unlikely. Shutouts are fairly
        /// rare and a 0-0 game would be kind of a double shutout. Also cannot happen in postseason.
        /// </summary>
        public class ZeroZeroIsLevelSeventyThreeScorigami { }
    }
}
